/// Pandora Button Enums
/// 
/// Defines all enums used by Pandora button components

/// Button variant styles
enum PandoraButtonVariant {
  primary,
  secondary,
  tertiary,
  ghost,
  link,
  outlined,
}

/// Button sizes
enum PandoraButtonSize {
  xs,
  sm,
  md,
  lg,
  xl,
}

/// Button states
enum PandoraButtonState {
  enabled,
  disabled,
  loading,
  success,
  error,
}

/// Icon position relative to text
enum IconPosition {
  start,
  end,
  top,
  bottom,
}
